<p id="qctxt">A qubit is a quantum system in which the Boolean states 0 and 1 are represented
 by a prescribed pair of normalised and mutually orthogonal quantum states labeled as {0,1} Sch95.
 The two states form a `computational basis' and any other (pure) state of the qubit can be written as
 a superposition α0+β1 for some α and β such that α2+β2=1. A qubit is typically a microscopic 
 system, such as an atom, a nuclear spin, or
a polarised photon. A collection of
n qubits is called a quantum register of size n..</p><b>
