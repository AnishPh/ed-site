<p id="qctxt">Quantum computing studies theoretical computation systems (quantum computers) that make
 direct use of quantum-mechanical phenomena, such as superposition and entanglement, to 
 perform operations on data.Quantum computers are different from digital electronic computers
 based on transistors. Whereas digital computers require data to be encoded into binary digits (bits),
 each of which is always in one of two definite states (0 or 1), quantum computation uses quantum bits 
 (qubits), which can be in superpositions of states.</p><b>
